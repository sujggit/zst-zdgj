public class ConfigTcipVO implements Serializable {

	private static final long serialVersionUID = 1L;
	private int id = Integer.MIN_VALUE;
	private String address;
	private String name;
	private String password;
	private int status = Integer.MIN_VALUE;
	private String portsName;

	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getAddress() {
		return address;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getPassword() {
		return password;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getStatus() {
		return status;
	}

	public void setPortsName(String portsName) {
		this.portsName = portsName;
	}

	public String getPortsName() {
		return portsName;
	}

}
