package com.zzst.model.meeting.centerControl;

import java.io.Serializable;

/**
 * class description: CenterControl Object
 * 
 * @date Thu Jun 28 15:18:31 CST 2012
 * @author ryan
 */
public class CenterControlVO implements Serializable {

	private static final long serialVersionUID = 1L;

	private String id;
	private String equipmentIP;
	private String command;
	private String equipmentType;
	private String equipmentName;
	private String ccIP;
	private String ccNO;
	private String controlInitDate;

	
	public String getCcIP() {
		return ccIP;
	}

	public void setCcIP(String ccIP) {
		this.ccIP = ccIP;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getId() {
		return id;
	}

	public void setEquipmentIP(String equipmentIP) {
		this.equipmentIP = equipmentIP;
	}

	public String getEquipmentIP() {
		return equipmentIP;
	}

	public void setCommand(String command) {
		this.command = command;
	}

	public String getCommand() {
		return command;
	}

	public void setEquipmentType(String equipmentType) {
		this.equipmentType = equipmentType;
	}

	public String getEquipmentType() {
		return equipmentType;
	}

	public void setEquipmentName(String equipmentName) {
		this.equipmentName = equipmentName;
	}

	public String getEquipmentName() {
		return equipmentName;
	}

	public void setCcNO(String ccNO) {
		this.ccNO = ccNO;
	}

	public String getCcNO() {
		return ccNO;
	}

	public void setControlInitDate(String controlInitDate) {
		this.controlInitDate = controlInitDate;
	}

	public String getControlInitDate() {
		return controlInitDate;
	}

}
