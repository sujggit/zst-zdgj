package com.zzst.model.meeting.meetingDebug;

import java.io.Serializable;
import java.sql.Timestamp;

/**
 * class description: MeetingDebug Object
 * 
 * @date Tue May 28 15:03:30 CST 2013
 * @author ryan
 */
public class MeetingDebugVO implements Serializable {

	private static final long serialVersionUID = 1L;

	private String id;
	private String meetingDetailId;
	private Timestamp debugStartTime;
	private Timestamp noticeTime;
	private String noticeType;
	private int noticeStatus = Integer.MIN_VALUE;

	public void setId(String id) {
		this.id = id;
	}

	public String getId() {
		return id;
	}

	public void setMeetingDetailId(String meetingDetailId) {
		this.meetingDetailId = meetingDetailId;
	}

	public String getMeetingDetailId() {
		return meetingDetailId;
	}

	public void setDebugStartTime(Timestamp debugStartTime) {
		this.debugStartTime = debugStartTime;
	}

	public Timestamp getDebugStartTime() {
		return debugStartTime;
	}

	public void setNoticeTime(Timestamp noticeTime) {
		this.noticeTime = noticeTime;
	}

	public Timestamp getNoticeTime() {
		return noticeTime;
	}

	public void setNoticeStatus(int noticeStatus) {
		this.noticeStatus = noticeStatus;
	}

	public int getNoticeStatus() {
		return noticeStatus;
	}

	public void setNoticeType(String noticeType) {
		this.noticeType = noticeType;
	}

	public String getNoticeType() {
		return noticeType;
	}

}
