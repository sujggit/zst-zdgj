public class TvVO {

	public static final String status_menu_on = "0";
	public static final String status_menu_off = "1";
	public static final String status_menu_def = "-1";
	
	public static final String status_power_on = "0";
	public static final String status_power_off = "1";
	public static final String status_power_def = "-1";
	
	private String id; //中控控制设备定义ID
	private String name;
	private String status_menu = status_menu_def;
	private String status_power = status_power_def;
	
	/**
	 * 设备是否可用
	 */
	private boolean available = true;

	public boolean isAvailable() {
		return available;
	}

	public void setAvailable(boolean available) {
		this.available = available;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStatus_menu() {
		return status_menu;
	}
	public void setStatus_menu(String status_menu) {
		this.status_menu = status_menu;
	}
	public String getStatus_power() {
		return status_power;
	}
	public void setStatus_power(String status_power) {
		this.status_power = status_power;
	}
	
	
}
