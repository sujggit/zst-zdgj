package com.zzst.centerContor.vo;


/**
 *@Description	矩阵对象
 *@date 2011-12-16下午04:29:18
 *@author ryan
 */
public class MatrixSwitchVO {

	private String id; //中控中设备定义ID
	private String ip;
	private String name;
	private String type;
	private String status;
	private String allSwitch;//所有的切换对应关系
	
	
	private String[][] in = {}; //0为ID，1为显示名称
	private String[][] out = {};//0为ID，1为显示名称
	
	/**
	 * 设备是否可用
	 */
	private boolean available = true;

	public boolean isAvailable() {
		return available;
	}

	public void setAvailable(boolean available) {
		this.available = available;
	}
	
	public String getAllSwitch() {
		return allSwitch;
	}
	public void setAllSwitch(String allSwitch) {
		this.allSwitch = allSwitch;
	}
	public String[][] getIn() {
		return in;
	}
	public void setIn(String[][] in) {
		this.in = in;
	}
	public String[][] getOut() {
		return out;
	}
	public void setOut(String[][] out) {
		this.out = out;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
}
