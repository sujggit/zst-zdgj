public class ExcuteResultVO {
	
	
	private boolean success = false;
	private String 	id;
	private String 	des;
	private Object 	object;
	
	public  ExcuteResultVO(){
	}
	
	public  ExcuteResultVO(boolean success,String str,Object obj){
		this.success = success;
		this.des = str;
		this.object = obj;
	}
	
	public boolean isSuccess() {
		return success;
	}
	
	public void setSuccess(boolean success) {
		this.success = success;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	public Object getObject() {
		return object;
	}
	public void setObject(Object object) {
		this.object = object;
	}
	
	
}
